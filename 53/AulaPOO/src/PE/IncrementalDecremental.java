package PE;

public class IncrementalDecremental {

    public static void main(String[] args) {

        int referencia;

        referencia = 100;

        System.out.println("O referencia foi: " + referencia);
        referencia++;
        System.out.println("O valor da referencia agora é: " + referencia);

        referencia--;

        //referencia--;

        System.out.println("O valor da referencia agora é: " + referencia);
    }
}